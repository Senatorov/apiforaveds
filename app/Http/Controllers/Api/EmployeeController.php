    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(EmployeeStoreRequest $request)
    {
        $request->validated();

        if ($request->hasFile('employee_photo')) {
            $image = $request->file('employee_photo');
            $path = $image->store('photo');
        }

        $employee = new Employee();
        $employee->employee_email = $request->employee_email;
        $employee->employee_name = $request->employee_name;
        $employee->employee_age = $request->employee_age;
        $employee->employee_experience = $request->employee_experience;
        $employee->employee_salary = $request->employee_salary;
        $employee->employee_photo = $path;

        $employee->save();

        session('success', 'Employee successfully added');


        return new EmployeeResource($employee);
    }

